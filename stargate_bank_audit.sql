SET LINESIZE 150;
COLUMN BANK_TICKER FORMAT A12;
COLUMN CURRENT_MCAP FORMAT A18;
COLUMN TARGET_18M FORMAT A18;
COLUMN TERACAP_DELTA FORMAT A18;
COLUMN MISSION_VECTOR FORMAT A25;

PROMPT =================================================================================
PROMPT         STARGATE v15.0 | 18-MONTH TERACAP ($) STRATEGIC AUDIT
PROMPT =================================================================================

SELECT 
    BANK_TICKER,
    '$' || TO_CHAR(CURRENT_MCAP, '999,990.99') || ' B' AS CURRENT_MCAP,
    CASE 
        WHEN TARGET_18M >= 1000 THEN '$' || TO_CHAR(TARGET_18M/1000, '990.99') || ' T'
        ELSE '$' || TO_CHAR(TARGET_18M, '999,990.99') || ' B'
    END AS TARGET_18M,
    '+$' || TO_CHAR(TARGET_18M - CURRENT_MCAP, '999,990.99') || ' B' AS TERACAP_DELTA,
    MISSION_VECTOR
FROM (
    SELECT 'JPM' AS BANK_TICKER, 891.18 AS CURRENT_MCAP, 1050.00 AS TARGET_18M, 'NII_DOMINANCE' AS MISSION_VECTOR FROM DUAL
    UNION ALL
    SELECT 'GS', 285.14, 345.00, 'EQUITY_VOL_CAPTURE' FROM DUAL
    UNION ALL
    SELECT 'MS', 282.45, 330.00, 'ASSET_LIGHT_GROWTH' FROM DUAL
    UNION ALL
    SELECT 'WFC', 265.42, 310.00, 'RETAIL_RECOVERY' FROM DUAL
    UNION ALL
    SELECT 'JEF', 9.35, 14.50, 'IB_SHARE_STEAL' FROM DUAL
) 
ORDER BY TARGET_18M DESC;

